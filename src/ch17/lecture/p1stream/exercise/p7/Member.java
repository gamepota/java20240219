package ch17.lecture.p1stream.exercise.p7;

public class Member {
    private String name;
    private String job;

    public Member(String name, String jov) {
        this.name = name;
        this.job = jov;
    }

    public String getName() {
        return name;
    }

    public String getJob() {
        return job;
    }
}
